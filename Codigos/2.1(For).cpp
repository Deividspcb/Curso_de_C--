#include <iostream>
using namespace std;

int main() {
    // Imprime uma mensagem inicial
    cout << "Vamos contar até 10!\n";

    // Loop for para contar até 10
    for (int i = 1; i <= 10; i++) { // Inicia um loop que irá iterar de 1 até 10
        cout << i << "\n"; // Imprime o valor atual de i
    } // Fim do loop

    // Mensagem de conclusão
    cout << "Chegamos ao 10! Fim do jogo.\n";

    return 0;
}
